function add(Addend1,Addend2){
    let sum=Addend1+Addend2;
    return sum;
}
function subtract(minuend,subtrahend){
    let difference=minuend-subtrahend;
    return difference;
}
function multiply(multiplicand, multipier){
    let product=multiplicand*multipier;
    return product;
}
